    private void btnGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarActionPerformed
        try {

            int i = Integer.parseInt(txtTamaño.getText());
            int m1[] = new int[i];
            String cad = "", cad0 = "Lista de valores original: \n";
            for (int u = 0; u < i; u++) {
                m1[u] = (int) (Math.random() * 50 + 1);
                cad += "[" + m1[u] + "]";
            }
            cad += "\n";
            txtOri.append(cad0 + cad + "\n");
            setTitle("Datos generados: " + txtTamaño.getText());
            vector = m1;
        } catch (java.lang.NumberFormatException error1) {
            showMessageDialog(null, "Ingrese un dígito válido.");
        }
    }//GEN-LAST:event_btnGenerarActionPerformed



    private void btnIntercalaciónActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIntercalaciónActionPerformed
           
        try {
            int y = Integer.parseInt(showInputDialog(this, "Ingrese el tamño del vector para Intercalación: "));

            int m1[] = new int[y];
            String cad = "", cad0 = "Lista de valores original: \n";
            for (int u = 0; u < y; u++) {
                m1[u] = (int) (Math.random() * 50 + 1);
                cad += "[" + m1[u] + "]";
            }
            cad += "\n";
            vector2 = m1;

            txtOri.append("Matriz generada para Intercalación\n" + cad + "\n");
            txtOri.append("Vector original ordenado:\n" + ordenar.Burbuja(vector) + "\n");
            txtOri.append("Vector auxiliar ordenado:\n" + ordenar.Burbuja(vector2) + "\n");
            txtOri2.append("Ordenados por método de Intercalación:\n" + ordenar.Intercalacion(vector, vector2) + "\n");
            lblMensaje.setText("Intercalación");

        } catch (java.lang.NullPointerException error1) {
            showMessageDialog(null, "Primero debe generar dígitos.");
        } catch (java.lang.NumberFormatException error2) {
            showMessageDialog(null, "Ingrese un valor del vector a intercalar.");
        }
    
    }//GEN-LAST:event_btnIntercalaciónActionPerformed

